<script>
    document.addEventListener('DOMContentLoaded', function () {
        const showPasswordCheckbox = document.getElementById('showPasswordCheck');
        const passwordInput = document.getElementById('passwordInput');
        const confirmPasswordInput = document.getElementById('confirmPasswordInput');
        const loginPasswordInput = document.getElementById('loginPasswordInput');
        const newPasswordInput = document.getElementById('newPasswordInput');
        const oldPasswordInput = document.getElementById('oldPasswordInput');


        if (showPasswordCheckbox) {
            showPasswordCheckbox.addEventListener('click', function () {
                const newType = showPasswordCheckbox.checked ? 'text' : 'password';

                if (passwordInput) passwordInput.type = newType;
                if (confirmPasswordInput) confirmPasswordInput.type = newType;
                if (loginPasswordInput) loginPasswordInput.type = newType;
                if (newPasswordInput) newPasswordInput.type = newType;
                if (oldPasswordInput) oldPasswordInput.type = newType;

            });
        }
    });
</script>


